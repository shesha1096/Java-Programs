package basic;

public class Pearl {
	private String colour;
	private String size;
	private String shape;
	
	
	public Pearl() {
		super();
	}
	public Pearl(String colour, String size, String shape) {
		super();
		this.colour = colour;
		this.size = size;
		this.shape = shape;
	}
	public String getColour() {
		return colour;
	}
	public void setColour(String colour) {
		this.colour = colour;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getShape() {
		return shape;
	}
	public void setShape(String shape) {
		this.shape = shape;
	}
	

}
